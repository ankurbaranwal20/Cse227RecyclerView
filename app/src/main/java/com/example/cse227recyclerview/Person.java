package com.example.cse227recyclerview;

public class Person {

    String name;
    int reg;

    public Person(String name, int reg) {
        this.name = name;
        this.reg = reg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getReg() {
        return reg;
    }

    public void setReg(int reg) {
        this.reg = reg;
    }

}
